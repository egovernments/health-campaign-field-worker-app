  /// Gets an IconData from the comprehensive icon map
  /// Returns null if the icon name is not found
  static IconData? getIcon(String iconName) {
    return _allIcons[iconName];
  }

  /// Gets an IconData with fallback to default
  static IconData getIconOrDefault(String iconName,
      [IconData defaultIcon = Icons.help]) {
    return getIcon(iconName) ?? defaultIcon;
  }

  /// Gets all available icon names
  static List<String> getAllAvailableIcons() {
    return _allIcons.keys.toList();
  }

  /// Checks if an icon name exists
  static bool hasIcon(String iconName) {
    return _allIcons.containsKey(iconName);
  }

  /// Gets icon count
  static int getIconCount() {
    return _allIcons.length;
  }
